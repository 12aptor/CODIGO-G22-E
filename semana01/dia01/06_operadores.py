""" Operadores aritméticos """
print(10 + 20) # Suma
print(20 - 10) # Resta
print(2 * 3) # Multiplicación
print(10 / 2) # División
print(10 // 2) # División entera
print(10 % 2) # Módulo
print(10 ** 2) # Potencia

""" Operadores de comparación """
print(2 == 2) # Igual
print(3 != 2) # Diferente
print(2 > 1) # Mayor que
print(2 < 3) # Menor que
print(2 >= 2) # Mayor o igual que
print(2 <= 2) # Menor o igual que

""" Operadores lógicos """
print(True and True) # And
print(True or False) # Or
print(not True) # Not

""" Operadores de asignación """
a = 10 # Asignación
a += 10 # Suma y asignación
a -= 10 # Resta y asignación
a *= 10 # Multiplicación y asignación
a /= 10 # División y asignación

""" Operadores de identidad """
print(3 is 3) # is (identidad)
print(3 is not 2) # is not (no identidad)

""" Operadores de membresía """
lista = [1, 2, 3]
print(1 in lista) # in (membresía)
print(4 not in lista) # not in (no membresía)