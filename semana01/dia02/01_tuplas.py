tupla = ("Manzana", "Pera", "Naranja")

""" Iterando sobre tuplas """
for fruta in tupla:
    print(fruta)

""" Métodos de tuplas """

""" Count - Devuelve el número de veces que aparece el valor """
print(tupla.count("Pera"))

""" Index - Devuelve el índice de la primera aparición """
print(tupla.index("Naranja"))